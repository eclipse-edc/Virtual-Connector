package org.eclipse.edc.virtual.transfer.fixtures.api;

import org.eclipse.edc.virtual.transfer.fixtures.VirtualConnectorClient;
import org.eclipse.edc.virtual.transfer.fixtures.api.model.AssetDto;
import org.eclipse.edc.virtual.transfer.fixtures.api.model.WithContext;

import static io.restassured.http.ContentType.JSON;
import static org.eclipse.edc.jsonld.spi.JsonLdKeywords.ID;

/**
 * API client for asset-related operations.
 */
public class AssetsApi {
    private final VirtualConnectorClient connector;

    public AssetsApi(VirtualConnectorClient connector) {
        this.connector = connector;
    }

    /**
     * Creates an asset in the specified participant context.
     *
     * @param participantContextId the participant context ID
     * @param asset                the asset to create
     * @return the ID of the created asset
     */
    public String createAsset(String participantContextId, AssetDto asset) {
        return connector.baseManagementRequest(participantContextId)
                .contentType(JSON)
                .body(new WithContext<>(asset))
                .when()
                .post("/v4alpha/participants/%s/assets".formatted(participantContextId))
                .then()
                .log().ifError()
                .statusCode(200)
                .contentType(JSON)
                .extract().jsonPath().getString(ID);
    }


}
